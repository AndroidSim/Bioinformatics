void get_default_pot(a_file *F, k_opt *op, float *Potn) 
{
 
  if(op->exam || op->learn || op->decoy)
    {
      if(op->einm) meller_elber_tom2(Potn);
      if(op->ein) meller_elber_tom1(Potn);
      if(op->eij) tobi_elber_eij(Potn);
      if(op->evdw) meller_elber_vdw(Potn); 
    }
  if(op->align) blosum50(Potn);
  if(op->strucmp) meller_elber_tom2_av(Potn);
}

